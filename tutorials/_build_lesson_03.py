"""
Build script for tutorials/03-glass-ir.ipynb.

This lesson is a hybrid — the user runs Gallowglass cells in the
Jupyter kernel, but the rendered Glass IR appears as static text in
markdown cells. The IR is regenerated each time the build script
runs, so committed pin hashes always match the current emit
discipline.
"""

