# Lazy: build once, reuse for every render call below.
_PRELUDE = None


def _prelude():
    global _PRELUDE
    if _PRELUDE is None:
        _PRELUDE = load_prelude()
    return _PRELUDE


def render_ir_fragment(src: str, name: str, module: str = 'Demo') -> str:
    """Compile ``src`` against the prelude and return the Glass IR
    fragment for ``name``. The fragment includes the pin hash, source
    Loc, and pretty-printed body — exactly what gets shown to a
    consumer of the typed IR layer."""
    prelude = _prelude()
    prog = parse(lex(src, '<demo>'), '<demo>')
    resolved, env = resolve(prog, module, prelude.module_envs, '<demo>')
    te, _, _ = _typecheck_capture(
        resolved, env, module, '<demo>',
        prior_te=prelude.type_env,
        prior_tc=prelude.type_constructors,
    )
    compiler = Compiler(
        module=module, pre_compiled=prelude.compiled,
        pre_class_methods=prelude.class_methods,
        pre_class_defaults=prelude.class_defaults,
        pre_class_constraints=prelude.class_constraints,
        pre_con_info=prelude.con_info,
    )
    compiled = compiler.compile(resolved)
    val = P(compiled[f'{module}.{name}'])
    pin_id = compute_pin_id(val)
    for d in resolved.decls:
        if hasattr(d, 'name') and d.name == name:
            return render_fragment(
                f'{module}.{name}', d,
                pin_id=pin_id, module=module, type_env=te,
            ).rstrip()
    raise RuntimeError(f'declaration {name!r} not found')


def md_with_ir(intro: str, src: str, name: str) -> str:
    """A markdown cell that shows the source plus its rendered Glass
    IR in a code block. The IR is regenerated by this script, so it
    always reflects the current emit behaviour."""
    ir = render_ir_fragment(src, name)
    return (f'{intro}\n\n'
            f'```\n{ir}\n```')


